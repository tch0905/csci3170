SELECT ENGINEER.ENGINEERID, ENGINEER.ENAME
FROM ENGINEER
JOIN RESEARCH ON ENGINEER.ENGINEERID = RESEARCH.ENGINEERID
JOIN PROJECT ON RESEARCH.PROJECTID = PROJECT.PROJECTID
WHERE PROJECT.PCATEGORY = 'Security'
GROUP BY ENGINEER.ENGINEERID, ENGINEER.ENAME
HAVING COUNT(DISTINCT PROJECT.PROJECTID) = (
    SELECT COUNT(*) 
    FROM PROJECT 
    WHERE PCATEGORY = 'Security'
)
ORDER BY ENGINEER.ENGINEERID DESC;

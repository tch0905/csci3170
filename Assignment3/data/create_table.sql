DROP TABLE RESEARCH;
DROP TABLE MAINTAIN;
DROP TABLE APPLICATION;
DROP TABLE PROJECT;
DROP TABLE ENGINEER;
DROP TABLE OFFICE;

CREATE TABLE OFFICE (
	OFFICEID INTEGER NOT NULL PRIMARY KEY,
	ONAME VARCHAR(255) NOT NULL,
	OADDRESS VARCHAR(255) NOT NULL,
	OPHONENUM VARCHAR(100) NOT NULL
);

CREATE TABLE ENGINEER (
	ENGINEERID INTEGER NOT NULL PRIMARY KEY,
	ENAME VARCHAR(255) NOT NULL ,
	ESALARY INTEGER NOT NULL ,
	OFFICEID INTEGER NOT NULL 
);

CREATE TABLE PROJECT (
	PROJECTID INTEGER NOT NULL PRIMARY KEY,
	OFFICEID INTEGER NOT NULL,
	PNAME VARCHAR(255) NOT NULL,
	PCATEGORY VARCHAR(255) NOT NULL,
	PBUDGET INTEGER NOT NULL,
	PEXPENDITURE INTEGER NOT NULL
);

CREATE TABLE APPLICATION (
	APPID INTEGER NOT NULL PRIMARY KEY,
	ANAME VARCHAR(255) NOT NULL,
	APRICE INTEGER NOT NULL,
	ADATE INTEGER NOT NULL,
	PROJECTID INTEGER NOT NULL
);

CREATE TABLE MAINTAIN (
	APPID INTEGER NOT NULL REFERENCES APPLICATION(APPID),
	ENGINEERID INTEGER NOT NULL REFERENCES ENGINEER(ENGINEERID)
);

CREATE TABLE RESEARCH (
	PROJECTID INTEGER NOT NULL REFERENCES PROJECT(PROJECTID),
	ENGINEERID INTEGER NOT NULL REFERENCES ENGINEER(ENGINEERID)
);

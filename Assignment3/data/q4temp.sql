SELECT subquery.ANAME, subquery.APRICE
FROM (
    SELECT A.ANAME, A.APRICE, ROW_NUMBER() OVER (ORDER BY A.APRICE ASC) AS row_num
    FROM APPLICATION A
    JOIN MAINTAIN M ON A.APPID = M.APPID
    JOIN ENGINEER E ON M.ENGINEERID = E.ENGINEERID
    WHERE E.ENAME = 'Chan Ling Yun'
)  subquery
WHERE subquery.row_num <= 3;

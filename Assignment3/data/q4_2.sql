SELECT ANAME, APRICE
FROM (
    SELECT ANAME, APRICE, ROW_NUMBER() OVER (ORDER BY APRICE ASC) AS rn
    FROM APPLICATION
    WHERE APPID IN (
        SELECT APPID
        FROM MAINTAIN
        WHERE ENGINEERID = (
            SELECT ENGINEERID
            FROM ENGINEER
            WHERE ENAME = 'Chan Ling Yun'
        )
    )
)
WHERE rn <= 3
ORDER BY APRICE DESC;